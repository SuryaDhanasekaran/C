/**
 * Variables and Literals
 * 
 * Data stored in runtime.
 * 
 */

#include<stdio.h>
int main()
{
    int a = 10;//assigning variables
    char b = '$';//double quotes - binary value
    float c = 12.5f;//f-float type(literals)
    long int d = 25892l;
    int aa = 0x41;
    float cc = 253e-2f;
    printf("%d \n %d \n %.2f",a,b,c);
    return 0;
}